import { spawnSync } from "node:child_process";
import { existsSync, mkdirSync } from "node:fs";
import path from "node:path";

import {
  generatedDartGrpcOutputDir,
  listProtoFiles,
  targetProtoDir,
  writeTextFile
} from "./utils.mjs";

const dartExecutable = process.env.DART_BIN ?? "dart";
const protoIncludeDir = targetProtoDir;
const outputDir = generatedDartGrpcOutputDir;

if (!existsSync(protoIncludeDir)) {
  console.error(
    `[contracts:grpc:generate:dart] Missing proto directory at ${protoIncludeDir}. Run pnpm contracts:grpc:sync first.`
  );
  process.exit(1);
}

const protoFiles = listProtoFiles(protoIncludeDir);

if (protoFiles.length === 0) {
  console.error(
    `[contracts:grpc:generate:dart] No .proto files found in ${protoIncludeDir}.`
  );
  process.exit(1);
}

mkdirSync(outputDir, { recursive: true });

console.log("[contracts:grpc:generate:dart] Ensuring Dart protoc plugin is installed...");
const activatePluginResult = spawnSync(
  dartExecutable,
  ["pub", "global", "activate", "protoc_plugin"],
  {
    stdio: "inherit",
    cwd: process.cwd()
  }
);

if (activatePluginResult.error) {
  console.error(
    `[contracts:grpc:generate:dart] Failed to install protoc_plugin: ${activatePluginResult.error.message}`
  );
  process.exit(1);
}

if (activatePluginResult.status !== 0) {
  process.exit(activatePluginResult.status ?? 1);
}

const protocArgs = [
  `-I${protoIncludeDir}`,
  `--dart_out=grpc:${outputDir}`,
  ...protoFiles
];

console.log(
  `[contracts:grpc:generate:dart] Generating Dart gRPC client from ${protoFiles.length} proto files...`
);

const protocResult = spawnSync("protoc", protocArgs, {
  stdio: "inherit",
  cwd: process.cwd()
});

if (protocResult.error) {
  console.error(
    `[contracts:grpc:generate:dart] Failed to execute protoc: ${protocResult.error.message}`
  );
  console.error(
    "[contracts:grpc:generate:dart] Install protoc and ensure it is available in PATH."
  );
  process.exit(1);
}

if (protocResult.status !== 0) {
  process.exit(protocResult.status ?? 1);
}

const readmePath = path.join(outputDir, "README.md");
const generatedAt = new Date().toISOString();
writeTextFile(
  readmePath,
  [
    "# role-node Dart gRPC generated SDK",
    "",
    "This directory is generated by:",
    "",
    "```bash",
    "pnpm contracts:grpc:generate:dart",
    "```",
    "",
    `Generated at: ${generatedAt}`,
    "",
    "Do not hand-edit generated files. Re-run generation after syncing proto contracts."
  ].join("\n")
);

console.log(
  `[contracts:grpc:generate:dart] Done. Output written to ${outputDir}`
);
